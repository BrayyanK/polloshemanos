CREATE SEQUENCE "PRODUCTO_SEQ"
	MINVALUE 1
	MAXVALUE 99999999999999
	INCREMENT BY 1
	START WITH 1000
	NOCACHE
	NOCYCLE;
	
CREATE TABLE NOTICIAS(

	ID					BIGINT				NOT NULL,
	TITULAR				VARCHAR(100)		,
	SUB_TITULAR			VARCHAR(150)		,
	CONTENIDO			VARCHAR(250)		,
	
	PRIMARY KEY (ID)

);

CREATE TABLE COMENTARIOS(

	ID_NOTICIA			BIGINT				NOT NULL,
	AUTOR				VARCHAR(100)		,
	COMENTARIO			VARCHAR(100)		,
	ESTRELLAS			INTEGER				,
	
	FOREIGN KEY (ID_NOTICIA) REFERENCES NOTICIAS (ID)

);

	
CREATE TABLE ALMACENES(

	ID					BIGINT				NOT NULL,
	METROS_CUADRADOS	INTEGER				,
	DIR					VARCHAR(200)		,
	MUNICIPIO			VARCHAR(150)		,
	CP					VARCHAR(10)			,
	PROVINCIA			VARCHAR(100)		,
	COUNTRY				VARCHAR(100)		,
	
	PRIMARY KEY (ID)

);

CREATE TABLE CLIENTES(

	ID					VARCHAR(10)			NOT NULL,
	NOMBRE				VARCHAR(150)		,
	DIRECCION			VARCHAR(200)		,
	POBLACION			VARCHAR(150)		,
	CODIGO_POSTAL		VARCHAR(10)			,
	PROVINCIA			VARCHAR(100)		,
	PAIS				VARCHAR(100)		,
	TELEFONO			VARCHAR(50)			,
	EMAIL				VARCHAR(200)		,
	
	PRIMARY KEY (ID)

);


CREATE TABLE ALUMNOS(

	ID					VARCHAR(50)			NOT NULL,
	NOMBRE_COMPLETO		VARCHAR(100)		,
	
	PRIMARY KEY (ID)

);

CREATE TABLE ASIGNATURAS(

	ID					VARCHAR(50)			NOT NULL,
	NOMBRE				VARCHAR(100)		,
	CREDITOS			INTEGER				,
	
	PRIMARY KEY (ID)

);

CREATE TABLE ASOCIA_ALUMNOS_ASIGNATURAS(

	ID_ALUMNO			VARCHAR(50)			NOT NULL,
	ID_ASIGNATURA		VARCHAR(50)			NOT NULL,
	
	PRIMARY KEY (ID_ALUMNO, ID_ASIGNATURA),
	FOREIGN KEY (ID_ALUMNO) REFERENCES ALUMNOS (ID),
	FOREIGN KEY (ID_ASIGNATURA) REFERENCES ASIGNATURAS (ID)

);


CREATE TABLE PRODUCTOS(

	CODIGO				BIGINT				NOT NULL,
	NOMBRE				VARCHAR(150)		,
	DESCRIPCION			VARCHAR(250)		,
	FECHA_ALTA			DATE				,
	PRECIO				DOUBLE				,
	FAMILIA				VARCHAR(50)			,
	DESCATALOGADO		TINYINT				,
	
	PRIMARY KEY (CODIGO)

);


CREATE TABLE EQUIPOS(

	ID					BIGINT				NOT NULL,
	NOMBRE				VARCHAR(100)		,
	NUMERO_TITULOS		INTEGER				,
	
	PRIMARY KEY(ID)

);

CREATE TABLE JUGADORES(

	ID					BIGINT				NOT NULL,
	NOMBRE				VARCHAR(100)		,
	ID_EQUIPO			BIGINT				,
	ORDEN				INTEGER				,
	
	PRIMARY KEY (ID),
	FOREIGN KEY (ID_EQUIPO) REFERENCES EQUIPOS (ID)

);



CREATE TABLE COCHES(

	MATRICULA			VARCHAR(8)			NOT NULL,
	FECHA_MATRICULACION	DATE				,
	MARCA				VARCHAR(100)		,
	MODELO				VARCHAR(100)		,
	PRECIO				DOUBLE				,
	NUMERO_PUERTAS		INTEGER				,
	SINIESTRADO			BOOLEAN				,
	MOTOR				INTEGER				,
	KILOMETROS			BIGINT				,
	
	PRIMARY KEY(MATRICULA)

);

CREATE TABLE ESTACIONES_METEOROLOGICAS(

	CODIGO				BIGINT				NOT NULL,
	NOMBRE				VARCHAR(100)		,
	FECHA_ALTA			DATE				,
	OPERATIVA			BOOLEAN				,
	LONGITUD			DOUBLE				,
	LATITUD				DOUBLE				,
	ESTADO				VARCHAR(14)			,
	
	PRIMARY KEY (CODIGO)

);

CREATE TABLE CANCIONES(

	ISBN				VARCHAR(30)			NOT NULL,
	TITULO				VARCHAR(200)		,
	GENERO				INTEGER				,
	FECHA				DATE				,
	DURACION			INTEGER				,
	
	PRIMARY KEY	(ISBN)

);


CREATE TABLE EMPLEADOS(

	DNI			VARCHAR(9)			NOT NULL,
	NOMBRE		VARCHAR(100)		,
	APELLIDO1	VARCHAR(100)		,
	APELLIDO2	VARCHAR(100)		,
	ACTIVO		BOOLEAN				,
	
	PRIMARY KEY (DNI)
 
);