CREATE TABLE ESTACIONES_METEOROLOGICAS(

	CODIGO		BIGINT				NOT NULL,
	NOMBRE		VARCHAR(100)		,
	FECHA_ALTA	DATE				,
	OPERATIVA	BOOLEAN				,
	LONGITUD	DOUBLE				,
	LATITUD		DOUBLE				,
	ESTADO		VARCHAR(14)			,
	
	PRIMARY KEY (CODIGO)

);

CREATE TABLE CANCIONES(

	ISBN		VARCHAR(30)			NOT NULL,
	TITULO		VARCHAR(200)		,
	GENERO		INTEGER				,
	FECHA		DATE				,
	DURACION	INTEGER				,
	
	PRIMARY KEY	(ISBN)

);


CREATE TABLE EMPLEADOS(

	DNI			VARCHAR(9)			NOT NULL,
	NOMBRE		VARCHAR(100)		,
	APELLIDO1	VARCHAR(100)		,
	APELLIDO2	VARCHAR(100)		,
	ACTIVO		BOOLEAN				,
	
	PRIMARY KEY (DNI)
 
);