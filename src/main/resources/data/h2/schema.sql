CREATE TABLE EQUIPOS(

	ID					BIGINT				NOT NULL,
	NOMBRE				VARCHAR(100)		,
	NUMERO_TITULOS		INTEGER				,
	
	PRIMARY KEY(ID)

);

CREATE TABLE JUGADORES(

	ID					BIGINT				NOT NULL,
	NOMBRE				VARCHAR(100)		,
	ID_EQUIPO			BIGINT				,
	ORDEN				INTEGER				,
	
	PRIMARY KEY (ID),
	FOREIGN KEY (ID_EQUIPO) REFERENCES EQUIPOS (ID)

);



CREATE TABLE COCHES(

	MATRICULA			VARCHAR(8)			NOT NULL,
	FECHA_MATRICULACION	DATE				,
	MARCA				VARCHAR(100)		,
	MODELO				VARCHAR(100)		,
	PRECIO				DOUBLE				,
	NUMERO_PUERTAS		INTEGER				,
	SINIESTRADO			BOOLEAN				,
	MOTOR				INTEGER				,
	KILOMETROS			BIGINT				,
	
	PRIMARY KEY(MATRICULA)

);

CREATE TABLE ESTACIONES_METEOROLOGICAS(

	CODIGO				BIGINT				NOT NULL,
	NOMBRE				VARCHAR(100)		,
	FECHA_ALTA			DATE				,
	OPERATIVA			BOOLEAN				,
	LONGITUD			DOUBLE				,
	LATITUD				DOUBLE				,
	ESTADO				VARCHAR(14)			,
	
	PRIMARY KEY (CODIGO)

);

CREATE TABLE CANCIONES(

	ISBN				VARCHAR(30)			NOT NULL,
	TITULO				VARCHAR(200)		,
	GENERO				INTEGER				,
	FECHA				DATE				,
	DURACION			INTEGER				,
	
	PRIMARY KEY	(ISBN)

);


CREATE TABLE EMPLEADOS(

	DNI			VARCHAR(9)			NOT NULL,
	NOMBRE		VARCHAR(100)		,
	APELLIDO1	VARCHAR(100)		,
	APELLIDO2	VARCHAR(100)		,
	ACTIVO		BOOLEAN				,
	
	PRIMARY KEY (DNI)
 
);